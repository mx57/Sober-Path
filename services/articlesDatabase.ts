// Валидация базы данных
export function validateArticlesDatabase(): {
  isValid: boolean;
  errors: string[];
} {
  const errors: string[] = [];
  
  // Проверка уникальности ID
  const ids = articlesDatabase.map(a => a.id);
  const uniqueIds = new Set(ids);
  if (ids.length !== uniqueIds.size) {
    errors.push('Обнаружены дублирующиеся ID статей');
  }
  
  // Проверка заполненности обязательных полей
  articlesDatabase.forEach((article, index) => {
    if (!article.title || article.title.trim().length === 0) {
      errors.push(`Статья ${index + 1}: отсутствует заголовок`);
    }
    if (!article.content || article.content.trim().length < 100) {
      errors.push(`Статья ${index + 1} (${article.title}): контент слишком короткий`);
    }
    if (!article.category) {
      errors.push(`Статья ${index + 1} (${article.title}): отсутствует категория`);
    }
    if (!article.preview) {
      errors.push(`Статья ${index + 1} (${article.title}): отсутствует превью`);
    }
    if (!article.tags || article.tags.length === 0) {
      errors.push(`Статья ${index + 1} (${article.title}): отсутствуют теги`);
    }
  });
  
  return {
    isValid: errors.length === 0,
    errors
  };
}

// Утилиты
export const getAllArticles = (): Article[] => articlesDatabase;

export const getArticleById = (id: string): Article | undefined => 
  articlesDatabase.find(article => article.id === id);

export const getArticlesByCategory = (category: string): Article[] => 
  category === 'Все' ? articlesDatabase : articlesDatabase.filter(article => article.category === category);

export const getCategories = (): string[] => 
  ['Все', ...Array.from(new Set(articlesDatabase.map(a => a.category))).sort()];

export const searchArticles = (query: string): Article[] => {
  const lowercaseQuery = query.toLowerCase().trim();
  return lowercaseQuery ? articlesDatabase.filter(article => 
    article.title.toLowerCase().includes(lowercaseQuery) ||
    article.preview.toLowerCase().includes(lowercaseQuery) ||
    article.content.toLowerCase().includes(lowercaseQuery) ||
    article.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  ) : [];
};

export const getRelatedArticles = (articleId: string): Article[] => {
  const article = getArticleById(articleId);
  return article ? articlesDatabase
    .filter(a => a.id !== articleId && (
      a.category === article.category ||
      a.tags.some(tag => article.tags.includes(tag))
    ))
    .slice(0, 5) : [];
};

export const getArticlesByDifficulty = (difficulty: 'beginner' | 'intermediate' | 'advanced'): Article[] =>
  articlesDatabase.filter(article => article.difficulty === difficulty);

export const getArticlesByTag = (tag: string): Article[] =>
  articlesDatabase.filter(article => article.tags.includes(tag));
