export const getArticleStats = () => {
  const stats = {
    total: articlesDatabase.length,
    byCategory: {} as Record<string, number>,
    byDifficulty: {
      beginner: 0,
      intermediate: 0,
      advanced: 0
    },
    avgReadTime: 0
  };
  
  articlesDatabase.forEach(article => {
    // –ü–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
    stats.byCategory[article.category] = (stats.byCategory[article.category] || 0) + 1;
    
    // –ü–æ —Å–ª–æ–∂–Ω–æ—Å—Ç–∏
    stats.byDifficulty[article.difficulty]++;
    
    // –°—Ä–µ–¥–Ω–µ–µ –≤—Ä–µ–º—è —á—Ç–µ–Ω–∏—è
    stats.avgReadTime += article.readTime;
  });
  
  stats.avgReadTime = Math.round(stats.avgReadTime / articlesDatabase.length);
  
  return stats;
};

