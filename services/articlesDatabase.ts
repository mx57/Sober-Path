export const searchArticles = (query: string): Article[] => {
  const lowercaseQuery = query.toLowerCase().trim();
  if (!lowercaseQuery) return [];
  
  return articlesDatabase.filter(article => 
    article.title.toLowerCase().includes(lowercaseQuery) ||
    article.preview.toLowerCase().includes(lowercaseQuery) ||
    article.content.toLowerCase().includes(lowercaseQuery) ||
    article.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery)) ||
    article.category.toLowerCase().includes(lowercaseQuery)
  );
};

export const getRelatedArticles = (articleId: string): Article[] => {
  const article = getArticleById(articleId);
  if (!article) return [];
  
  // –ï—Å–ª–∏ –µ—Å—Ç—å —è–≤–Ω–æ —É–∫–∞–∑–∞–Ω–Ω—ã–µ —Å–≤—è–∑–∞–Ω–Ω—ã–µ —Å—Ç–∞—Ç—å–∏
  if (article.relatedArticles && article.relatedArticles.length > 0) {
    return article.relatedArticles
      .map(id => getArticleById(id))
      .filter((a): a is Article => a !== undefined);
  }
  
  // –ò–Ω–∞—á–µ –Ω–∞—Ö–æ–¥–∏–º –ø–æ—Ö–æ–∂–∏–µ —Å—Ç–∞—Ç—å–∏ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –∏ —Ç–µ–≥–∞–º
  return articlesDatabase
    .filter(a => a.id !== articleId)
    .filter(a => 
      a.category === article.category ||
      a.tags.some(tag => article.tags.includes(tag))
    )
    .sort((a, b) => {
      const aScore = (a.category === article.category ? 2 : 0) +
                     a.tags.filter(tag => article.tags.includes(tag)).length;
      const bScore = (b.category === article.category ? 2 : 0) +
                     b.tags.filter(tag => article.tags.includes(tag)).length;
      return bScore - aScore;
    })
    .slice(0, 5);
};

